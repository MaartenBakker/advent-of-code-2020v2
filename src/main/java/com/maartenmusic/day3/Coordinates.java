package com.maartenmusic.day3;

public class Coordinates {
    private int X;
    private int Y;

    public Coordinates() {
        this.X = 0;
        this.Y = 0;
    }

    public int getX() {
        return X;
    }

    public void setX(int x) {
        this.X = x;
    }

    public int getY() {
        return Y;
    }

    public void setY(int y) {
        this.Y = y;
    }
}
